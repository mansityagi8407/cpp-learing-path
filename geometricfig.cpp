#include<iostream>
using namespace std;
int main() {
    float b = 8 , h = 4 , l = 2 , s = 3 , r = 6;
    cout<<"Area of Triangle : ";
    cout<<(b*h)/2<<endl;
    cout<<"Area of Rectangle : ";
    cout<<l*b<<endl;
    cout<<"Area of Square : ";
    cout<<s*s<<endl;
    cout<<"Area of Circle : ";
    cout<<3.14*r*r<<endl;
}